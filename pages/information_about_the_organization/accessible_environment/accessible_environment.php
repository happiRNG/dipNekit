<p style="text-indent:20px;" align="justify">В столовой могут быть выделены столики для обучающихся с ограниченными возможностями здоровья по зрению, которые располагаются в непосредственной близости от стойки выдачи пищи. Дежурный администратор может осуществлять помощь при приеме пищи.



<p style="text-indent:20px;" align="center"> 
<b>
Информация о специальных условиях охраны здоровья для инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">Для охраны здоровья обучающихся в колледже работает медицинский кабинет. Проводится вакцинация студентов, оказывается первая медицинская помощь.
 

