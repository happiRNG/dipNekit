<p style="text-indent:20px;" align="justify">Информационно-техническое обеспечение обучающихся в колледже включает в себя персональные компьютеры, все из них  доступны для использования обучающимися в свободное от основных занятий время, имеют доступ к учебной электронной библиотечной системе и доступ в сеть Интернет.


