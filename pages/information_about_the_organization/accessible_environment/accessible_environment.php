<p style="text-indent:20px;" align="center"> 
<b>

Информация о специально оборудованных учебных кабинетах для обучения инвалидов и лиц с ограниченными возможностями здоровья</p></b>

<p style="text-indent:20px;" align="justify">В государственном автономном профессиональном образовательном  учреждении «Оренбургский областной медицинский колледж» (далее — Колледж) для дополнительной индивидуализированной коррекции нарушений учебных и коммуникативных умений, профессиональной и социальной адаптации обучающихся с ограниченными возможностями здоровья и инвалидов в программу подготовки специалистов среднего звена введены адаптационные дисциплины:
<p style="text-indent:20px;" align="justify">— Психология личности и профессиональное самоопределение;
<p style="text-indent:20px;" align="justify">— Коммуникативный практикум.
<p style="text-indent:20px;" align="justify">Обучение для лиц с инвалидностью и ОВЗ (при необходимости) организуется на первом этаже учебного корпуса. Пути подхода к учебным кабинетам оснащены тактильными знаками.
<p style="text-indent:20px;" align="justify">Выбор методов обучения в каждом отдельном случае обуславливается целями обучения, содержанием обучения, исходным уровнем, имеющихся знаний, умений, навыков, наличием методического и материально-технического обеспечения, особенностями восприятия информации обучающимися, и т.д.
<p style="text-indent:20px;" align="justify">Обучающиеся инвалиды и лица с ограниченными возможностями здоровья могут быть обеспечены при необходимости печатными и электронными образовательными ресурсами в формах, адаптированных к ограничениям их здоровья. 


<p style="text-indent:20px;" align="center"> 
<b>Информация о приспособленных объектах для проведения практических занятий для обучения инвалидов и лиц с ограниченными возможностями здоровья
</b>
<p style="text-indent:20px;" align="justify">Специалистами колледжа совместно с центром занятости проводится работа по подготовке к трудоустройству выпускников из числа обучающихся с ограниченными возможностями здоровья и инвалидов и их закреплению на рабочих местах посредством организации презентации и встреч с работодателями, индивидуальных консультации, мастер-классов, тренингов.


<p style="text-indent:20px;" align="center"> 
<b>
Информация о приспособленной библиотеке для обучения инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">Библиотека колледжа обеспечена доступом обучающихся инвалидов и лиц с ограниченными возможностями здоровья к учебным и методическим изданиям в электронной форме, в том числе через электронные библиотечные системы.

<p style="text-indent:20px;" align="center"> 
<b>
Информация о приспособленных объектах спорта для обучения инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">Для проведения занятий физической культурой лицами с ограниченными возможностями здоровья и с инвалидностью имеется в наличии спортивный зал,  комната оборудованная специализированными тренажерами общеукрепляющей направленности. Фитнес-тренажеры отвечают требованиям доступности, надежности, прочности. Ответственный преподаватель физической культуры проводит занятия в тренажерном и спортивном зале с учетом вида и тяжести нарушений организма обучающегося.

<p style="text-indent:20px;" align="center"> 
<b>
Информация о приспособленных средствах обучения и воспитания для обучения инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">Выбор методов, форм и средств обучения и воспитания в колледже осуществляется, исходя из их доступности для обучающихся с ограниченными возможностями здоровья и с инвалидностью.
<p style="text-indent:20px;" align="justify">Осуществляется психолого-педагогическое, социальное, медицинско-оздоровительное сопровождение, в том числе предоставляются услуги физиотерапевтического отделения поликлиники №5 ГАУЗ «ГКБ №3» г.Оренбурга, а также волонтерская помощь инвалидам и лицам с ограниченными возможностями здоровья.


<p style="text-indent:20px;" align="center"> 
<b>
Информация об обеспечении беспрепятственного доступа в здание образовательной организации и общежитие для обучения инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">Одним из приоритетных направлений деятельности колледжа является создание безбарьерной среды, направленное на улучшение доступности и качества образования. 
<p style="text-indent:20px;" align="justify">Доступность образования для маломобильных обучающихся в здании ГАПОУ «Оренбургский областной медицинский колледж», по ул. Ленинской д. 25 обеспечивают: устройство пандусов, поручней, кнопки вызова, доступные входные группы, наличие в общежитии комнаты для проживания инвалидов.
<p style="text-indent:20px;" align="justify">В Колледже обеспечивается информационная открытость  организации образовательного процесса для лиц с ограниченными возможностями здоровья и их родителей. 






<p style="text-indent:20px;" align="center"> 
<b>
Информация о специальных условиях питания для инвалидов и лиц с ограниченными возможностями здоровья</b>

<p style="text-indent:20px;" align="justify">
