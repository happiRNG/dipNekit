</a> 
<p style="text-indent:20px;" align="justify">

<p style="text-indent:20px;" align="justify">В связи с принятием постановления Правительства Оренбургской области  от 24 ноября 2020 г. № 988-пп «О внесении изменений в постановление Правительства Оренбургской области от 28 июня 2019 года №441-пп «О нормативах и правилах формирования стипендиального фонда в государственных профессиональных образовательных организациях, находящихся в ведении органов исполнительной власти Оренбургской области, за счет ассигнований областного бюджета»
 <p style="text-indent:20px;" align="justify"> с 01.09.2020 года установлены следующие размеры стипендий для студентов, обучающихся в ГАПОУ «ООМК» по очной бюджетной форме обучения:
<p style="text-indent:20px;" align="justify">1. Государственная академическая стипендия по результатам промежуточной аттестации в следующих размерах
<p style="text-indent:20px;" align="justify">- обучающимся на «отлично» в размере 1000 руб.00 коп.,
<p style="text-indent:20px;" align="justify">- детям-сиротам, обучающимся на «отлично» 1500 руб. 00 коп.,
<p style="text-indent:20px;" align="justify">- обучающимся на «хорошо» и «отлично» или на «хорошо» в размере 524 руб. 40 коп.,
<p style="text-indent:20px;" align="justify">- детям-сиротам, обучающимся на «хорошо» и «отлично» или на «хорошо» 787 руб. 75 коп.   
<p style="text-indent:20px;" align="justify">2.  Государственная социальная стипендия в размере 787 руб. 75 коп. 








 <br>
 <br>
<p style="text-indent:20px;" align="justify">Колледж располагает  женским общежитием на 180 мест, оборудованным душевыми, бытовыми комнатами, а также необходимым инвентарем. В  комнатах для самоподготовки студенты   готовятся к учебным занятиям. Студенческий Совет под руководством воспитателя организует досуг проживающих. В актовом зале проводятся интересные воспитательные мероприятия, выездные заседания предметных кружков, встречи с интересными людьми.

