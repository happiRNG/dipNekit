<br>
<br><b>II. Пользователи библиотеки обязаны:</b>
<br>10.Соблюдать правила пользования библиотекой;
<br>11.бережно относиться к произведениям печати(не вырывать страниц, не делать в книгах подчеркиваний, пометок),иным документам на различных носителях, оборудованию, инвентарю ;
<br>12.пользоваться ценными и справочными документами только в помещении библиотеки;
<br>13.убедиться при получении документов в отсутствии дефектов, а при обнаружении проинформировать об этом работника библиотеки, ответственность за обнаруженные дефекты в сдаваемых документах несет последний пользователь;
<br>14.расписываться в читательском формуляре за каждый полученный документ;
<br>15.возвращать документы в библиотеку в установленные сроки;
<br>16.заменять документы библиотеки в случае их утраты или порчи им равноценными, либо компенсировать ущерб в размере, установленном правилами пользования библиотекой;
<br>17.полностью рассчитаться с библиотекой по истечении учебного года или окончания учебы в колледже;
<br>
<br><b>III.Порядок пользования библиотекой:</b>
<br>1.запись обучающихся в колледже в библиотеку производится в индивидуальном порядке, 2.перерегистрация пользователей библиотеки производится ежегодно;
<br>3.документом, подтверждающим право пользования библиотекой,
является читательский формуляр;
<br>4.читательский формуляр фиксирует дату выдачи пользователю документов из фонда библиотеки и их возвращения в библиотеку.
<br>
<br><b>IV.Порядок пользования абонементом:</b>
<br>1.пользователи имеют право получать на дом из многотомных изданий не более двух документов одновременно;
<br>2.максимальные сроки пользования документами:
    <br>  &nbsp;&nbsp;&nbsp;а) учебники,учебные пособия- учебный год;
    <br>  &nbsp;&nbsp; б) научно-популярная, познавательная, художественная литература-1месяц;
    <br>  &nbsp;&nbsp;&nbsp;в) периодические издания, издания повышенного спроса-15 дней;
<br>3.пользователи могут продлить срок пользования документами, если на них отсутствует спрос со стороны других пользователей.
<br>

<br><b>Порядок пользования читальным залом</b>
<br>1.Выдача литературы в читальном зале осуществляется по предъявлению студенческого билета . Читатели при получении изданий расписываются на книжном формуляре. Книжный формуляр является документом удостоверяющим дату и факт выдачи читателю и приема библиотекарем книг и других произведений печати.
<br>2.Число книг, других произведений печати и иных материалов, выдаваемых в читальных залах, как правило, не ограничивается. При наличии единовременного повышенного спроса число выдаваемых экземпляров может быть ограничено(по решению администрации библиотеки).
<br>3.Литература из фонда читального зала выдается по устному запросу читателей.
<br>4.Энциклопедии,справочные издания выдаются только в читальном зале.
<br>5.Выносить литературу из читального зала запрещено.
