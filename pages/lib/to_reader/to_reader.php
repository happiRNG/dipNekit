  <br> &nbsp;5.совершенствовать библиотечное и информационно-библиографическое обслуживание читателей, внедряя компьютеризацию и передовую технологию;
  <br> &nbsp;6.обеспечивать высокую культуру обслуживания: оказывать читателям помощь в выборе необходимых произведений печати и иных материалов, проводя устные консультации, предоставляя в их пользование каталоги и иные формы информирования, организуя книжные выставки, библиографические обзоры, дни информации и другие мероприятия;
