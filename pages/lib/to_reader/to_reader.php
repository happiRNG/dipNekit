<br>1.получать полную информацию о составе библиотечного фонда, информационных ресурсах и предоставляемых библиотекой услугах;
<br>2.пользоваться справочно - библиографическим аппаратом библиотеки;
<br>3. Получать консультационную помощь в поиске и выборе источников информации;
<br>4.получать во временное пользование на абонементе и в читальном зале печатные издания и другие источники информации;
<br>5.продлевать срок пользования документами;
<br>6.получать тематические, фактографические, уточняющие и библиографические справки;
<br>7.получать консультационную помощь в работе с информацией на нетрадиционных носителях при пользовании электронным оборудованием ;
<br>8.учавствовать в мероприятиях, проводимых библиотекой;
